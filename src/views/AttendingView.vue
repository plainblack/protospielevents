    <h1>Attending a Protospiel</h1>

    <p>Here are <a href="http://protospiel-milwaukee.org/materials/Protospiel_101.pdf">a few things you should know</a> about attending a Protospiel.</p>
    
    <h2>Etiquette</h2>
    
    <p>All designers are asked to honor a reciprocal agreement; if you ask 5 people to play your game for an hour, you should play at least 5 hours of other people's games.</p>
    
    <p>As a general rule, after 1 hour you should take the temperature of the people at your table and see if they are ready to give feedback or if they would like to keep going.</p>
    
    <h2>Expectations of Designers</h2>
    
    <p><strong>Mention your design goals from the start.</strong> Tell your playtesters as specifically as you can what they should look at or look for in your game or where you're looking for advice.</p>
    
    <p><strong>Be respectful of everyone's time.</strong> Practice your setup and rules explanation and try to be as efficient as you can. It is okay to interrupt play to talk over details and variations, but stay on track - there's a lot to do and other people to share with!</p>
    
    <p><strong>Be open to feedback and suggestions!</strong> Your fellow designers will likely have a wealth of thoughts and ideas to share about your game; take advantage of this, but don't let misunderstandings or differences of opinion get in the way.</p>
    
    <h2>Expectations of Play Testers</h2>
    
    <p><strong>Listen carefully to the designer and try to provide the help for which they're looking.</strong> For example, suggestions about graphic design may not be very useful if the session is about rules or balance.</p>
    
    <p><strong>Fill out a playtest evaluation.</strong> Or make notes and draw pictures; it can be very helpful to a designer to have feedback recorded. Try to present your thoughts at the end of the session to avoid interruptions.</p>
    
    <p><strong>Focus on being constructive and helpful.</strong> A designer's game is likely something they've labored over and care deeply about. They're sharing their creation to try to make it better and it can be very discouraging if feedback is dismissive or thin.</p>
    
    <h2>Do I have to have a game to playtest to attend Protospiel?</h2>
    
    <p>Not at all! We always welcome people who are willing to help. If you haven't had time to finish your prototype in time for the event or are just starting out, you can still be a big help and make some great connections. It is best if you have some experience with design and playtesting. Many of the people in attendance will have put in considerable time and money to prepare for the event and are looking for the best help they can get. Everyone will be looking for insightful and serious feedback from like-minded designers. But all you really need is a willingness to play games and a desire to help make them better.</p>
    
    <h2>How many games should I bring?</h2>
    
    <p>You're welcome to bring as many prototypes as you want, but it will be difficult to find time for more than two or three of them. Protospiel depends on a cooperative ethic where people take turns sharing and working on their designs. You'll get much better feedback than a typical playtest session but that requires sharing your insights with others on their projects too. And sometimes you can learn as much from playtesting other's games as you can from showing your own game.</p>
    
    <h2>What stage of development should my games be in?</h2>
    
    <p>Try to bring games that have already undergone some amount of development and refinement. It's best if it isn't completely finished, but also a good idea if it isn't just a vague outline. This is meant to be a working session that improves your game.</p>
    
    <p>The appearance of your prototype is not nearly as important as the functionality. Some people will have demonstration copies with excellent production quality, but that's not necessary to get good work done on a game.</p>
    
