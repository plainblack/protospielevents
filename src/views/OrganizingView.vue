    <h1>Organizing a Protospiel Event</h1>

    <p>Organizing a Protospiel event is both rewarding and challenging, but not nearly as challenging as running a regular board game convention.</p>
    
    <h2>Budget</h2>
    
    <p>Every Protospiel is different.  Generally speaking you'll charge $50 for a full weekend badge for a designer and $10 for a full weekend badge for a play tester. These will be your primary sources of revenue.</p>
    
    <p>Your venue (usually a hotel) cost will be your largest expense by far. Venue costs will vary from location to location. For your first year plan for around 50 attendees. A space for this many attendees will likely cost between $1,000 and $2,000. So assuming it costs you $2,000 and you only sell designer badges, you'll need to sell 40 badges just to break even.</p>
    
    <h2>Finding a Venue</h2>
    
    <p>Long before you announce your event, you need to find a venue. Most likely this will be a hotel or convention center. If you live in a big enough city, you'll have a local convention and visitors bureau that can help you find a venue. However, it's just as easy usually to pull up <a href="http://maps.google.com">Google Maps</a> and search for "Convention Space in <em>Your City</em>". Other search terms include "Event Space" and "Meeting Space". Then just start contacting them.</p>
    
    <p>Don't mess around with venues that take more than 2 business days to get back to you. They will just cause you headaches later. Likewise, you usually don't want to go with the really cheap or really expensive places. Also avoid hotels that require you to be on the hook for a reserved hotel room block, or that require a huge amount of catering.</p>
    
    <p>When talking to a hotel it may be useful to describe this as a "meeting" rather than a party or convention. So that they understand you won't be needing food.</p>
    
    <h2>Venue Layout</h2>
    
    <p>Most hotels will not have a default table setup that will work for a game convention. You'll need to get specific with them. What you are looking for are 30 inch by 6 foot tables with 2 chairs on each side. </p>
    
    <p>If you want, you can include a couple of 8 rounds, but those aren't as convenient, and won't get used as much. Plus 8 rounds take up a lot more space.</p>
    
    <h2>Registration</h2>
    
    <p>Setting up a registration system can be both complicated and expensive. Luckily Protospiel has a friend in this regard called <a href="https://tabletop.events">Tabletop.Events</a>. Tabletop.Events will handle registration, build you a web site, manage your mailing list, and print your badges and has very reasonable <a href="https://tabletop.events/pricing">pricing</a>.</p>
    
    <h2>Snacks &amp; Meals</h2>
    
    <p>Don't initially include snacks or beverages other than water.  If you get enough attendance to cover your basic costs, then start adding things like coffee, tea, hot chocolate, soda, pretzels, chips, fruit, etc.</p>
    
    <p>Don't ever include meals. We've done surveys on this, and people's reactions are all over the board. Some of it comes from not wanting extra expenses attached to the badge. Some of it comes from people having religious or allergy related dietary restrictions. </p>
    
    <h2>Sponsors</h2>
    
    <p>It will be very hard finding sponsors your first year unless you are well connected to your community. That said, here are the types of people you should reach out to:</p>
    
    <ul>
    <li><a href="https://www.thegamecrafter.com">The Game Crafter</a> - Will provide you with free prototyping materials.</li>
    <li>Local restaurants - Will often provide you with coupons or other promo items to give to your attendees for food discounts.</li>
    <li>Local publishers, game stores, and luminaries - Will sometimes sponsor something like coffee, or soda in exchange for advertising.</li>
    </ul>
    
    <h2>Publishers</h2>
    
    <p>Though you don't need to have publishers attend a Protospiel, one of the draws for game designers is to get some feedback from publishers. Unless you are well connected, you likely will not get a publisher to travel to your Protospiel. Instead, look for game publishers in your local region. </p>
    
    <h2>Event Setup and Schedule</h2>
    
