    <h1>What is Protospiel?</h1>

    <p>Protospiel is an event where you can bring a board or card game prototype to play with other designers, play testers, and publishers. It's a great way to break your game and to fix it. It doesn't matter what level of polish your game has. Many games are made right on the spot at Protospiel. But there are also many games there that are just doing final testing before they are published or put out on Kickstarter.</p>
    
    <p>If you are a game designer looking to get professional level feedback from other designers there is no better place to get that feedback. You'll have the opportunity to play test your designs many times and the friendships you'll make will last a lifetime.</p>
    
    <p>If you're a gamer who likes to try out new things, then Protospiel is also likely the best experience you'll have. You'll get to see dozens if not hundreds of new designs in a single weekend. Plus you'll get to shape them before they hit the shelves of stores or the pages of Kickstarter.</p>
    
